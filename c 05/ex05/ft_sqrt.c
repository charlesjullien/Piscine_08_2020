int	ft_sqrt(int nb)
{
	long int	x;
	int			res;

	x = 1;
	if (nb == 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while (x * x < nb)
	{
		x++;
	}
	if ((x * x) == nb)
	{
		res = x;
		return (res);
	}
	else
		return (0);
}
