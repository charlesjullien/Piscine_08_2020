#include <unistd.h>

void	ft_putnbr(int nb)
{
	char		c;
	long int	nbr;

	nbr = nb;
	if (nbr < 0)
	{
		nbr = nbr * (-1);
		write(1, "-", 1);
	}
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
	}
	c = (nbr % 10) + 48;
	write(1, &c, 1);
}
